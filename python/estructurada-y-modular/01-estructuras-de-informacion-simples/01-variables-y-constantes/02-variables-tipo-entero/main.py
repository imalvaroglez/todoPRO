# Variables tipo entero (int) en Python

# Declaración de variables enteras
edad = 25
cantidad = 100
anio_nacimiento = 1999

# Imprimir valores
print(f"Edad: {edad}")
print(f"Cantidad: {cantidad}")
print(f"Año de nacimiento: {anio_nacimiento}")

# Operaciones aritméticas con enteros
suma = edad + 5
resta = cantidad - 20
multiplicacion = edad * 2
division = 100 // 2  # División entera

print(f"\nSuma (edad + 5): {suma}")
print(f"Resta (cantidad - 20): {resta}")
print(f"Multiplicación (edad * 2): {multiplicacion}")
print(f"División entera (100 // 2): {division}")
